package variable;
/*
        변수는 하나씩 선언 할 수 도있다.
        변수는 한번에 여러 개를 선언 할 수 도있다.
*/
public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 10; // 변수 초기화
        System.out.println(a);
        a = 50;
        System.out.println(a);

        int b = 20; // 변수 선언 + 초기화
        int c = 30, d = 40; // 여러 변수 선언 + 초기화

    }
}
